/**
 * Catalyst Watch — "On Deck" feature.
 *
 * 3 timed drops per day:
 *   9:15 AM ET  — Pre-Market Catalyst Watch (overnight earnings, analyst upgrades, breaking news)
 *   12:00 PM ET — Midday Movers (morning news cycle, analyst actions, social buzz shifts)
 *   3:30 PM ET  — Power Hour Watch (tonight's earnings, late-breaking news, momentum)
 *
 * Each drop: 3 stocks with catalyst reasons + strength rating (1-3 flames).
 * End-of-day recap shows how picks performed.
 */
